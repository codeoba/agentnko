export async function seedDefaultTemplates(db, userId) {
  // Angalia kama templates za mfumo zipo tayari
  const existing = await db.get(
    'SELECT COUNT(*) as count FROM quick_reply_templates WHERE user_id = ?',
    [userId]
  );
  
  if (existing.count > 0) {
    console.log(`Templates already seeded for user ${userId} (${existing.count} found).`);
    return;
  }

  console.log(`Seeding ${PREMIUM_TEMPLATES.length} premium templates for user ${userId}...`);
  
  const stmt = await db.prepare(
    `INSERT INTO quick_reply_templates (user_id, name, shortcut, category, content, usage_count, created_at)
     VALUES (?, ?, ?, ?, ?, 0, datetime('now'))`
  );

  for (const tpl of PREMIUM_TEMPLATES) {
    await stmt.run(userId, tpl.name, tpl.shortcut, tpl.category, tpl.content);
  }

  await stmt.finalize();
  console.log(`✅ ${PREMIUM_TEMPLATES.length} premium templates seeded successfully!`);
}
